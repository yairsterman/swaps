var mongoose = require('mongoose');
var UserSchema = new mongoose.Schema({
    firstName: String,
    lastName: String,
    displayName: String,
    email: String,
    gender: String,
    age: Number,
    ocupation: String,
    aboutMe: String,
    facebookId: String,
    image: String,
    country: String,
    city: String,
    address: String,
    swaps: Number,
    traveling: Boolean,
    travelingDest: Array,
    travelingInfo: Array,
    departure: Date,
    returnDate: Date,
    rating: Number,
    requests: Array,
    notifications: Array,
    messages: Array,
    reviews: Array,
    photos: Array,
    apptInfo: {},
    paymentInfo: {},
    updated_at: { type: Date, default: Date.now },
});
module.exports = mongoose.model('User', UserSchema);
