var mongoose = require('mongoose');
var Schema = mongoose.Schema;
var UserSchema = new Schema({
    firstName: String,
    lastName: String,
    displayName: String,
    email: String,
    gender: Number,
    birthday: String,
    occupation: String,
    aboutMe: String,
    facebookId: String,
    googleId: String,
    image: String,
    country: String,
    city: String,
    address: String,
    swaps: Number,
    allowViewHome: Boolean,
    traveling: Boolean,
    travelingDest: [{
        dates: String,
        departure: Number,
        returnDate: Number,
        guests: Number,
        destination: String,
    }],
    travelingInfo: Array,
    departure: Date,
    returnDate: Date,
    rating: Number,
    requests: [{ type: Schema.Types.ObjectId, ref: 'Request' }],
    notifications: Array,
    messages: Array,
    reviews: Array,
    photos: Array,
    favorites: Array,
    thingsToDo: Array,
    radius: {},
    location: {},
    apptInfo: {
        amenities: Array,
        rules: Array,
        roomType: Number,
        propertyType: Number,
        title: String,
        about: String,
        moreInfo: String,
        beds: Number,
        baths: Number,
        guests: Number,
        rooms: Number,
        bedType: Number,
    },
    paymentInfo: {},
    transactions: [{ type: Schema.Types.ObjectId, ref: 'Transaction' }],
    deposit: Number,
    updated_at: { type: Date, default: Date.now },
});
module.exports = mongoose.model('User', UserSchema);
