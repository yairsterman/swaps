var mongoose = require('mongoose');
var UserSchema = new mongoose.Schema({
    firstName: String,
    lastName: String,
    displayName: String,
    email: String,
    gender: Number,
    birthday: String,
    occupation: String,
    aboutMe: String,
    facebookId: String,
    image: String,
    country: String,
    city: String,
    address: String,
    swaps: Number,
    traveling: Boolean,
    travelingDest: Array,
    travelingInfo: Array,
    departure: Date,
    returnDate: Date,
    rating: Number,
    requests: Array,
    notifications: Array,
    messages: Array,
    reviews: Array,
    photos: Array,
    favorites: Array,
    thingsToDo: Array,
    radius: {},
    location: {},
    apptInfo: {
        amenities: Array,
        rules: Array,
        roomType: Number,
        propertyType: Number,
        title: String,
        about: String,
        moreInfo: String,
        beds: Number,
        baths: Number,
        guests: Number,
        rooms: Number,
        bedType: Number,
    },
    paymentInfo: {},
    deposit: Number,
    updated_at: { type: Date, default: Date.now },
});
module.exports = mongoose.model('User', UserSchema);
